package com.mygdx.game.models;

/**
 * An interface for an x,y position on the board.
 */
public interface Position {

    /**
     * Returns the x-position.
     *
     * @return the x-position
     */
    int getX();

    /**
     * Returns the y-position.
     *
     * @return the y-position
     */
    int getY();

}
